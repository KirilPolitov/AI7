# =========================
# КАЧВАНЕ НА СНИМКА
# =========================

uploaded_file = st.file_uploader(
    "📁 Качи изображение",
    type=["jpg", "jpeg", "png"]
)

camera_image = st.camera_input("📷 Направи снимка")

image = None

if uploaded_file:
    image = Image.open(uploaded_file)

elif camera_image:
    image = Image.open(camera_image)

# =========================
# OCR АНАЛИЗ
# =========================

if image is not None:

    st.image(image, caption="Качено изображение", use_container_width=True)

    image = image.convert("RGB")

    enhancer = ImageEnhance.Contrast(image)
    image = enhancer.enhance(1.5)

    img_array = np.array(image)

    @st.cache_resource
    def load_reader():
        return easyocr.Reader(['bg', 'en'])

    reader = load_reader()

    with st.spinner("🤖 AI анализира етикета..."):
        results = reader.readtext(img_array)

    detected_text = " ".join([res[1] for res in results])
    detected_text = detected_text.lower()

    st.subheader("📄 Разпознат текст")

    st.text_area(
        "Текст от етикета:",
        detected_text,
        height=220
    )

    # =========================
    # ТЪРСЕНЕ НА ВЕЩЕСТВА
    # =========================

    found = []

    for ingredient in harmful_ingredients:

        pattern = r'\b' + re.escape(ingredient.lower()) + r'\b'

        if re.search(pattern, detected_text):
            found.append((ingredient, harmful_ingredients[ingredient]))

    # Премахване на дублиране
    found = list(set((i, str(d)) for i, d in found))
    found = [(i, eval(d)) for i, d in found]

    # =========================
    # РЕЗУЛТАТИ
    # =========================

    st.subheader("⚠️ Анализ на съставките")

    if found:

        risk_score = len(found)

        if risk_score <= 3:
            st.success(f"🟢 Нисък риск ({risk_score} открити вещества)")
        elif risk_score <= 7:
            st.warning(f"🟡 Среден риск ({risk_score} открити вещества)")
        else:
            st.error(f"🔴 Висок риск ({risk_score} открити вещества)")

        st.markdown("## Открити вредни съставки")

        for ingredient, data in sorted(found):

            risk_icon = {
                "Нисък": "🟢",
                "Среден": "🟡",
                "Висок": "🔴"
            }

            icon = risk_icon.get(data["risk"], "⚪")

            with st.expander(
                f"{icon} {ingredient.upper()} — {data['name']}"
            ):

                st.markdown("### 🧪 Вещество")
                st.write(data["name"])

                st.markdown("### ⚠️ Възможни ефекти")
                st.write(data["effects"])

                st.markdown("### 🫀 Засегнати органи")
                st.write(data["organs"])

                st.markdown("### 📊 Ниво на риск")
                st.write(f"{icon} {data['risk']} риск")

                st.markdown("### 💡 Препоръка")
                st.info(data["advice"])

    else:
        st.success("✅ Не са открити вредни съставки.")

